package org.example;

public class Product {
    String name,brand,type,expiry,quantity,price,sellingPrice,totalPrice;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getExpiry() {
        return expiry;
    }

    public void setExpiry(String expiry) {
        this.expiry = expiry;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getSellingPrice() {
        return sellingPrice;
    }

    public void setSellingPrice(String sellingPrice) {
        this.sellingPrice = sellingPrice;
    }

    public String getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(String totalPrice) {
        this.totalPrice = totalPrice;
    }

    public Product(String name, String brand, String type, String expiry, String quantity, String price, String sellingPrice, String totalPrice) {
        this.name = name;
        this.brand = brand;
        this.type = type;
        this.expiry = expiry;
        this.quantity = quantity;
        this.price = price;
        this.sellingPrice=sellingPrice;
        this.totalPrice=totalPrice;
    }
}
